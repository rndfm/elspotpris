<h1>Cookie- og privatlivspolitik</h1>
<h2>Introduktion</h2>
<p>Når du besøger vores hjemmeside indsamles der oplysninger om dig, som anvendes til at tilpasse og forbedre vores indhold, hjemmesidens brugervenlighed og annoncernes relevans, i forhold til dine præferencer. Hvis du ikke ønsker, at der indsamles disse oplysninger om dig, bør du slette dine cookies 
, eller fravælge de cookies du ikke ønsker indsamlet. Nedenfor har vi uddybet, hvilke informationer der indsamles, deres formål og hvilke tredjeparter der har adgang til dem.</p>

<h2>Cookies</h2>
<p>Hjemmesiden anvender "cookies", som er en tekstfil, der gemmes på din computer, mobil, tablet eller anden enhed du bruger for at du kan tilgå denne hjemmeside. Cookies har til formål at genkende hjemmesiden, huske indstillinger, forbedre brugeroplevelsen, udføre statistik og målrette annoncer. Cookies kan ikke indeholde skadelig kode som f.eks. virus.
Det er muligt at slette eller blokere for cookies. Hvis du sletter eller blokerer cookies, vil du kunne opleve at brugeroplevelsen og designet bliver forringet, samt at annoncer bliver mindre relevante for dig og optræder hyppigere. Idet cookies i høj grad anvendes til at sikre brugerfunktionaliteten, kan du desuden risikere, at hjemmesiden ikke fungerer optimalt, samt at der er indhold, du ikke kan få adgang til.</p>

<p>Vi gør opmærksom på, at alle hjemmesider på internettet indsamler cookies. Jævnfør den nye datalov om GDPR fra 2018, er det et lovkrav at brugere skal give samtykke til, at hjemmesider må indsamle disse dataoplysninger. 
Vi bruger cookies til statistik og målrettet markedsføring fra os selv og fra 3. parter. Se hvilke 3. parter vi benytter os af her: Google (statistikcookies), og…</p>

<h2>Personoplysninger</h2>
<p>Personoplysninger er alle slags informationer, der i et eller andet omfang kan henføres til dig. Når du benytter vores hjemmeside indsamler og behandler vi en række informationer om dig. Det sker f.eks. ved alm. tilgang af indhold på hjemmesiden.</p>

<p>Vi indsamler og behandler typisk følgende typer af oplysninger, når du besøger vores hjemmeside:</p>
<ul>
    <li>Et unikt ID og tekniske oplysninger om din computer, tablet eller mobiltelefon</li>
    <li>Dit IP-nummer</li>
    <li>Geografisk placering</li>
    <li>Hvilke sider du klikker på</li>
</ul>
<p>I denne forbindelse deles dine oplysninger med tredjeparten Google Analytics for at udføre statistik og optimering af hjemmesiden.</p>

<h2>Sikkerhed</h2>
<p>Vi behandler dine personoplysninger sikkert og fortroligt i overensstemmelse med gældende lovgivning, herunder persondataforordningen og databeskyttelsesloven.
Dine oplysninger vil alene blive anvendt til det formål, de er indsamlet til, og vil blive slettet, når dette formål er opfyldt eller ikke længere er relevant.
Vi har truffet tekniske og organisatoriske foranstaltninger mod, at dine oplysninger hændeligt eller ulovligt bliver slettet, offentliggjort, fortabt, forringet eller kommer til uvedkommendes kendskab, misbruges eller i øvrigt behandles i strid med lovgivningen.</p>

<p>Ønsker du at få indsigt, rette eller slette data, kan du altid kontakte os. Såfremt at indholdet er lovligt at slette i henhold til lovgivning, kan vi efterkomme dette ønske.</p>

<h2>Formål</h2>
<p>Oplysningerne bruges til at optimere vores services og indhold.</p>

<h2>Periode for opbevaring</h2>
<p>Oplysningerne opbevares i det tidsrum, der er tilladt i henhold til lovgivningen, og vi sletter dem, når de ikke længere er nødvendige. Perioden afhænger af oplysningernes karakter og baggrunden for opbevaring. Det er derfor ikke muligt at angive en generel tidsramme for, hvornår informationer slettes.</p>

<h2>Videregivelse af oplysninger</h2>
<p>Data om din brug af hjemmesiden, geografisk placering m.v. videregives til tredjeparter i det omfang disse oplysninger er kendt. Du kan se hvilke tredjeparter, der er tale om, i afsnittet om "Cookies" ovenfor.
Vi benytter herudover en række tredjeparter til opbevaring og behandling af data. Disse behandler udelukkende oplysninger på vores vegne og må ikke anvende dem til egne formål.</p>

